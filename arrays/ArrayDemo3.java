package com.evergent.corejava.arrays;

public class ArrayDemo3 {
	public static void main(String[] args) {
		int arr[] = new int[5];
		for (int i = 0; i < 5; i++) {
			arr[i] = i;// Declaring the Values
		}
		for (int i = 0; i < 5; i++) {
			System.out.println(arr[i]);// Accessing the ValuesF
		}
	}

}
